fn main() {
    //整数相加
    println!("1 + 2 = {}", 1u32+2);

    //整数相减
    println!("1 - 2 = {}", 1i32 -2 );

    //布尔值操作逻辑
    println!("true && false is {}", true && false);
    println!("true || false is {}", true || false);
    println!("not true is {}", !true);

    //使用下划线来提高可读性
    println!(" one million is writtern as {}", 1_000_000u32);
}
